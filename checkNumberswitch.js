let number = parseInt(prompt("Enter a number between 1 and 5:"));

switch(number) {
    case 1:
        alert("One");
        break;
    case 2:
        alert("Two");
        break;
    case 3:
        alert("Three");
        break;
    case 4:
        alert("Four");
        break;
    case 5:
        alert("Five");
        break;
    default:
        alert("Invalid number! Please enter a number between 1 and 5.");
}
